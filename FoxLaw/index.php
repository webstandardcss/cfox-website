                        <div class="entry-content">
                            <p>Everyone’s been there or may be there at least one time in their life. &nbsp;You’re driving along when red and blue flashing lights consume your rear view mirror, and BAM you get a ticket.&nbsp; Did you know the State of Texas
                                can suspend your driver’s license if you accumulate too many tickets, or you fail to address an outstanding ticket. If you fail to pay a traffic ticket and miss a court date, a failure to appear (FTA) warrant may be issued
                                for your arrest. So the question is,” What do you do?” Let’s explore some options you may have:</p>
                            <p><strong>Option #1:</strong> You could choose to ignore the ticket by not paying the fine and hope to avoid being arrested, but this is your WORST OPTION.</p>
                            <p><em><strong>THE RESULT</strong>:</em> More than likely, you will end up paying the fine plus additional fees, AND you could be denied renewal of your driver’s license.</p>
                            <p><strong>Option #2:</strong> You could plead “guilty” or “no contest” and pay the fine or apply jail time to cover the fine.</p>
                            <p><em><strong>THE RESULT:</strong></em> The ticket would be recorded as a conviction on your driving record, AND could result in higher insurance rates, Texas surcharges, or a license suspension.</p>
                            <p><strong>Option #3:</strong> Your BEST OPTION may be to plead “not guilty” and hire legal representation, such as the Law Office of Christina Fox.</p>
                            <p><em><strong>THE RESULT: </strong></em>We will not only post a bond to lift the warrant (if applicable), but also look at the current traffic laws and make sure your rights (and driving record) are protect.&nbsp; You will no
                                longer be subject to arrest, and you will have the opportunity to fight your traffic ticket.</p>
