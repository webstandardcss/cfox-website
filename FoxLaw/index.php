                        <h2 class="entry-title">How Surcharges Are Assessed In Texas</h2>

                        <div class="entry-meta">
                            <span class="meta-prep meta-prep-author">Posted on</span><span class="entry-date"> January 4, 2012 </span><span class="meta-sep">by</span> <span class="author vcard">CFox Law</span> </div>
                        <!-- .entry-meta -->

                        <div class="entry-content">
                            <p style="color: #000000; text-align: center;"><strong>LET THE LAW OFFICE OF CHRISTINA FOX HELP YOU GET YOUR LICENSE ISSUES TAKEN CARE OF TODAY!</strong></p>
                            <p style="color: #000000; text-align: center;"><strong>817.479.0535</strong></p>
                            <p style="color: #000000;">Many people find themselves a part of the surcharge program without any knowledge on how it resulted.&nbsp; This is intended to explain the surcharge system and how to avoid it.</p>
                            <p style="color: #000000;">There are a few ways that a person can become part of the surcharge program.&nbsp; First, if you are convicted of certain traffic related offenses, you will automatically be placed in the surcharge system.&nbsp; An example
                                of some of the charges that will cause an automatic surcharge to be assessed is listed below:</p>
                            <table style="color: #000000;" width="98%" cellspacing="4" cellpadding="6" border="0" bgcolor="#dedede" align="center">
                                <tbody>
                                    <tr>
                                        <td style="color: #000000;" valign="top" bgcolor="#ededed">
                                            <div><strong>Type of Conviction</strong></div>
                                        </td>
                                        <td style="color: #000000;" width="73" valign="top" bgcolor="#ededed">
                                            <div><strong>Surcharge</strong><br> Per year for 3 years</div>
                                        </td>
                                    </tr>
                                    <tr>
                                        <td style="color: #000000;" valign="top" bgcolor="#fefefe"><strong>1st Driving While Intoxicated (DWI) Offense</strong><br> Texas or out-of state conviction</td>
                                        <td style="color: #000000;" width="73" valign="top" bgcolor="#fefefe">$1,000</td>
                                    </tr>
                                    <tr>
                                        <td style="color: #000000;" valign="top" bgcolor="#fefefe"><strong>Subsequent DWI&nbsp;</strong><br> Texas or out-of state conviction</td>
                                        <td style="color: #000000;" width="73" valign="top" bgcolor="#fefefe">$1,500</td>
                                    </tr>
                                    <tr>
                                        <td style="color: #000000;" valign="top" bgcolor="#fefefe"><strong>DWI with blood alcohol concentration of 0.16 or more</strong><br> Texas or out-of state conviction</td>
                                        <td style="color: #000000;" width="73" valign="top" bgcolor="#fefefe">$2,000</td>
                                    </tr>
                                    <tr>
                                        <td style="color: #000000;" valign="top" bgcolor="#fefefe"><strong>No Insurance</strong></td>
                                        <td style="color: #000000;" width="73" valign="top" bgcolor="#fefefe">$250</td>
                                    </tr>
                                    <tr>
                                        <td style="color: #000000;" valign="top" bgcolor="#fefefe"><strong>Driving While License Invalid</strong><br> For example: license is canceled, suspended, or revoked</td>
                                        <td style="color: #000000;" width="73" valign="top" bgcolor="#fefefe">$250</td>
                                    </tr>
                                    <tr>
                                        <td style="color: #000000;" valign="top" bgcolor="#fefefe"><strong>No Driver License</strong><br> For example: no driver license, expired license, no commercial driver license, or endorsement violations</td>
                                        <td style="color: #000000;" width="73" valign="top" bgcolor="#fefefe">$100</td>
                                    </tr>
                                </tbody>
                            </table>
                            <p style="color: #000000;">Other ways of becoming part of the surcharge program are through multiple convictions for moving violations.&nbsp; For example getting too many speeding ticket convictions over a 3 year period.&nbsp; This will cause surcharges
                                to be assessed in the amount of $100.00 for the first 6 points assessed and an extra $25.00 for each point thereafter.</p>
                            <p style="color: #000000;">If you think you may have a surcharge issue that could be suspending your license we encourage you to visit the Texas DPS website to determine what the status of your license is in.&nbsp;&nbsp;
