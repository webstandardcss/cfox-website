                        <h2 class="entry-title">6 Easy steps you can take to clear up your license</h2>

                        <div class="entry-meta">
                            <span class="meta-prep meta-prep-author">Posted on</span><span class="entry-date"> May 6, 2014 </span><span class="meta-sep">by</span> <span class="author vcard">CFox Law</span> </div>
                        <!-- .entry-meta -->

                        <div class="entry-content">
                            <p style="color: #000000;" align="center">Suspended License?</p>
                            <p style="color: #000000;" align="center">6 Easy steps you can take to clear up your license</p>
                            <p style="color: #000000;">Has your license been suspended, but you still need to drive to get to work or school?&nbsp; Do you have family members that you need to run errands for?&nbsp; Do you have a household that depends on you to be able to drive?&nbsp;
                                Don’t let having a suspended license stop you from doing these things.&nbsp; An occupational license may be an option for you.</p>
                            <p style="color: #000000;">1.&nbsp;&nbsp;<span>Pay off your surcharges with the State of Texas if you have any</span></p>
                            <p style="color: #000000;">The state of Texas has surcharges for convictions on certain offenses such as driving while intoxicated, driving while your license is invalid or expired, not having insurance, or having too many points on your license within
                                a certain period.&nbsp; These surcharges can result in your license being suspended for up to two years after paying the surcharge amount.&nbsp; There are payment plans available to pay surcharges and in some instances
                                you can even get surcharges reduced if the amount of money you make is within the guidelines for the&nbsp;<a href="https://www.txsurchargeonline.com/%28S%282hvdp21dyxwbrjj5rc34y2ek%29%29/Indigence.aspx">indigency program</a>&nbsp;set
                                by Texas Department of Public Safety (DPS).&nbsp; For each conviction of certain driving offenses a surcharge can last for three years.&nbsp; For more information on&nbsp;<a href="https://www.txsurchargeonline.com/%28S%28ja5iwmao0qz10ovrv5svmzpt%29%29/onlineservices.aspx">surcharges</a>&nbsp;and
                                to see if you have any&nbsp;<a href="https://www.txsurchargeonline.com/%28S%28ja5iwmao0qz10ovrv5svmzpt%29%29/onlineservices.aspx">click here</a></p>
                            <p style="color: #000000;">2.&nbsp;&nbsp;<span>Resolve any tickets</span></p>
                            <p style="color: #000000;">Our firm can research this for you for a nominal fee.&nbsp; Don’t let your tickets turn into warrants because you do not think you have the money to resolve them.&nbsp; Did you know that when you sit tickets out in jail or
                                do community service, this usually results in a guilty conviction on your driving record?&nbsp; Don’t just plead “guilty” to the offenses before finding out from our office what this conviction will do to your driving record.&nbsp;
                                Our firm may be able to keep offenses off your driving record and protect you from increased insurance, suspended licenses, and Texas surcharges.</p>
                            <p style="color: #000000;">3.&nbsp;&nbsp;<span>Take care of any outstanding warrants</span></p>
                            <p style="color: #000000;">Warrants for traffic violations can be a reason why your license is suspended.&nbsp; Sometimes, simply taking care of a warrant on a traffic violation can take your license out of a suspended status.&nbsp; Our firm can file
                                a surety bond for any warrants you may have regarding traffic violations.&nbsp; Filing a surety bond with the courts, will lift your warrant(s) usually within 24 hours.&nbsp; In most instances, the fee for our firm to file
                                a surety bond on your traffic warrants is cheaper than using a bonds bailsman.&nbsp; With the&nbsp;<a href="http://austintexas.gov/department/warrant-roundup">2013 Great Texas Warrant Roundup</a>&nbsp;, many citizens are
                                finding themselves inconvenienced and embarrassed by getting arrested for outstanding warrants.&nbsp; Do not let this be you, call us today!</p>
                            <p style="color: #000000;">4.&nbsp; If your license is still suspended after handling tickets, surcharges, and warrants, maybe you need an occupational license</p>
                            <p style="color: #000000;">Occupational licenses are available for people who have suspended licenses if they meet certain criteria determined by the law.&nbsp; It is possible to get you back on the road again within a week from getting your license
                                suspended.&nbsp; An occupational license can last for up to two years, and will allow you to drive to work, school, and to run household errands.&nbsp; You must maintain a driving log and SR-22 insurance while you have
                                an occupational license.&nbsp; Don’t put yourself at risk by continuing to drive with a suspended license, it will only lead to more surcharges and legal issues.&nbsp; Call our office, 817-519-8404, to see if your situation
                                qualifies you to get an occupational license.</p>
                            <p style="color: #000000;">5.&nbsp;&nbsp;<span>Secure SR-22 insurance</span></p>
                            <p style="color: #000000;">Contact your insurance provider and find out about getting an SR-22 insurance certificate.&nbsp; You can have an SR-22 certificate with liability or full coverage insurance.&nbsp; The cost varies by insurance provider, but
                                in most situations SR-22 coverage can be affordable.</p>
                            <p style="color: #000000;">6.&nbsp;&nbsp;<span><a href="https://txapps.texas.gov/txapp/txdps/dleligibility/login.do?errorReason=session">Pay off reinstatement fees with the Texas Department of Public Safety</a></span></p>
                            <p style="color: #000000;">Prior to making any payments for reinstatement fees make sure that you handle your warrants, tickets, and surcharges.&nbsp; To find out if you have any reinstatement fees or if your license is suspended,&nbsp;<a href="https://txapps.texas.gov/txapp/txdps/dleligibility/login.do?errorReason=session">click here.</a></p>
                            <p style="color: #000000;">We can help you navigate through these steps and get you back driving legally on the road.</p>
                            <p style="color: #000000;" align="center">If your license is suspended, then contact the&nbsp;<a href="http://www.cfoxlawoffice.com/">Law Office of Christina Fox,</a>&nbsp;817-519-8404, to discuss getting an occupational license.&nbsp; An occupational license will
                                allow you to drive to work or your trade, perform household duties and errands, or go to attend school.</p>
                            <p style="color: #000000;">Contact us today to find out our specials and flat fees regarding occupational licenses.</p>
                            <p style="color: #000000;">Disclaimer: This information is not being offered for or intended to be legal advice.&nbsp; Please contact our firm today to speak with an attorney for legal advice about your specific situation.</p>
